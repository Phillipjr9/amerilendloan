import cron from 'node-cron';
import * as db from '../db';
import { 
  sendPaymentReceiptEmail, 
  sendPaymentFailureEmail 
} from './email';

/**
 * Auto-Pay Scheduler
 * Runs daily at 2:00 AM to process scheduled automatic payments
 */

let isProcessing = false;
let lastRunTime: Date | null = null;
let processedCount = 0;
let failedCount = 0;

export function startAutoPayScheduler() {
  console.log('[Auto-Pay Scheduler] Initializing auto-pay scheduler...');
  
  // Run every day at 2:00 AM
  cron.schedule('0 2 * * *', async () => {
    console.log('[Auto-Pay Scheduler] Starting scheduled auto-pay processing...');
    await processAutoPayments();
  });

  // Also allow manual trigger for testing
  console.log('[Auto-Pay Scheduler] ✅ Scheduler started (runs daily at 2:00 AM)');
}

export async function processAutoPayments() {
  if (isProcessing) {
    console.log('[Auto-Pay Scheduler] Already processing, skipping...');
    return {
      success: false,
      message: 'Auto-pay processing already in progress',
    };
  }

  isProcessing = true;
  lastRunTime = new Date();
  processedCount = 0;
  failedCount = 0;

  try {
    console.log('[Auto-Pay Scheduler] Fetching auto-pay settings due today...');
    
    // Get all active auto-pay settings where today is the payment day
    const today = new Date();
    const currentDay = today.getDate();
    
    const autoPaySettings = await db.getAutoPaySettingsDueToday(currentDay);
    console.log(`[Auto-Pay Scheduler] Found ${autoPaySettings.length} auto-pay settings due today`);

    for (const setting of autoPaySettings) {
      try {
        await processIndividualAutoPay(setting);
        processedCount++;
      } catch (error) {
        console.error(`[Auto-Pay Scheduler] Error processing auto-pay ${setting.id}:`, error);
        failedCount++;
        
        // Record failure
        await db.recordAutoPayFailure(setting.id);
        
        // Send failure notification
        const user = await db.getUserById(setting.userId);
        if (!setting.loanId) {
          console.error(`[Auto-Pay Scheduler] ❌ Loan ID missing for auto-pay ${setting.id}`);
          return;
        }
        const loan = await db.getLoanApplicationById(setting.loanId);
        
        if (user && loan) {
          await sendPaymentFailureEmail(
            user.email || '',
            user.firstName || 'User',
            loan.trackingNumber,
            setting.amount || 0,
            error instanceof Error ? error.message : 'Unknown error'
          );
        }

        // Disable auto-pay after 3 consecutive failures
        if (setting.failedAttempts >= 2) { // This will be the 3rd failure
          await db.updateAutoPaySetting(setting.id, { isEnabled: false });
          console.log(`[Auto-Pay Scheduler] Disabled auto-pay ${setting.id} after 3 consecutive failures`);
        }
      }
    }

    console.log(`[Auto-Pay Scheduler] ✅ Processing complete: ${processedCount} succeeded, ${failedCount} failed`);
    
    return {
      success: true,
      processedCount,
      failedCount,
      totalSettings: autoPaySettings.length,
    };
  } catch (error) {
    console.error('[Auto-Pay Scheduler] Fatal error during auto-pay processing:', error);
    return {
      success: false,
      message: error instanceof Error ? error.message : 'Unknown error',
    };
  } finally {
    isProcessing = false;
  }
}

async function processIndividualAutoPay(setting: any) {
  console.log(`[Auto-Pay Scheduler] Processing auto-pay ${setting.id} for user ${setting.userId}`);
  
    // Get user and loan details
    const user = await db.getUserById(setting.userId);
    if (!setting.loanId) {
      throw new Error('Loan ID is missing from auto-pay setting');
    }
    const loan = await db.getLoanApplicationById(setting.loanId);  if (!user || !loan) {
    throw new Error('User or loan not found');
  }

  if (loan.status !== 'disbursed') {
    throw new Error('Loan is not in disbursed status');
  }

  // TODO: Production Implementation Required
  // The auto-pay system requires integration with saved payment methods:
  // 1. Store tokenized payment methods when user enables auto-pay
  // 2. Use Authorize.net Customer Profile IDs or Stripe Payment Method IDs
  // 3. Charge using stored tokens instead of processing new card transactions
  // 
  // Current implementation uses mock payment processing for development/testing
  
  const mockTransactionId = `AUTO_${Date.now()}_${setting.id}`;
  
  // Record payment in database (using mock transaction ID)
  const payment = await db.createPayment({
    loanApplicationId: loan.id,
    userId: user.id,
    amount: setting.amount,
    currency: 'USD',
    paymentProvider: 'authorizenet', // Mock auto-pay using authorizenet provider
    paymentMethod: 'card',
    paymentIntentId: mockTransactionId,
    cardLast4: '0000', // TODO: Get from saved payment method
    cardBrand: 'Mock', // TODO: Get from saved payment method
    status: 'succeeded',
  });

  // Update auto-pay next payment date and reset failure count
  const nextPaymentDate = new Date();
  nextPaymentDate.setMonth(nextPaymentDate.getMonth() + 1);
  
  await db.updateAutoPaySetting(setting.id, {
    nextPaymentDate,
  });

  // Send success email
  await sendPaymentReceiptEmail(
    user.email || '',
    user.firstName || 'User',
    loan.trackingNumber,
    setting.amount || 0,
    'card',
    mockTransactionId
  );

  if (payment) {
    console.log(`[Auto-Pay Scheduler] ✅ Successfully processed auto-pay ${setting.id}, payment ID: ${payment.id}`);
  }
  console.log('[Auto-Pay Scheduler] ⚠️  Using mock payment - integrate saved payment methods for production');
}

export function getSchedulerStatus() {
  return {
    isProcessing,
    lastRunTime,
    processedCount,
    failedCount,
  };
}

// Manual trigger for testing
export async function triggerManualAutoPayProcessing() {
  console.log('[Auto-Pay Scheduler] Manual trigger requested');
  return await processAutoPayments();
}
